package main

import (
	"bytes"
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestEmptyMsg(t *testing.T) {
	b := bytes.Buffer{}
	NSDPDefaultMsg.WriteToBuffer(&b)

	empty := []byte{
		0x01,
		0x01,
		0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00,
		0x00, 0x01,
		0x4E, 0x53, 0x44, 0x50,
		0x00, 0x00, 0x00, 0x00,
		0xff, 0xff, 0x00, 0x00,
		//		0x00, 0x00, 0xff, 0xff,
	}

	assert.Equal(t, empty, b.Bytes())
}
